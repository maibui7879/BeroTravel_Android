package com.example.berotravel20.data.models.User;

public class User {
    private String _id;
    private String name;
    private String email;
    private String role;

    public String getId() {
        return _id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }
}
